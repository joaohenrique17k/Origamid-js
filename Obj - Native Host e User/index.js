/*
    Native

    Objetos nativos são aqueles definidos na especificação da linguagem e são 
    implementados independente do host 

    Contrutores de objetos nativos

    Object 
    String 
    Array
    Function

    (Objetos espeficicados pela lingugem.)

 --------------------------------------------------------------------

    Host

   Objetos do host são aqueles implementados pelo próprio ambiente. 
   Por exemplo no browser possuímos objetos do DOM, como DomList, 
   HTMLCollection e outros. Em Node.js os objetos do Host são diferentes,
    já que não estamos em um ambiente do browser.

    // Objetos do browser
    NodeList
    HTMLCollection
    Element


    (Objetos espeficicados pelo ambiente.)

     --------------------------------------------------------------------

     User 
     
     São objetos criados pelo próprio usuário. Ou seja quando você cria por exemplo 
     um objeto chado pessoa e coloca nele valores como nome, sobrenome, etc. Também é considerado 
     Objetos User quando importamos qualquer tipo de biblioteca. 

     (Objetos espeficicados pelo usuário.)
*/
