 // SÍNCRONO vs ASSÍNCRONO

/*
    Síncrono

    Espera uma tarefa acabar para começar a outra
    (Passa por uma linha de cada vez)


    =>----------------------------------------


    Assíncrono

    Não espera uma tarefa acabar para começar outra. Ele vai executando as outras
    até receber as informações necessárias.

    Exemplos(Assíncronos): setTimeout, Ajax, Promises, fetch e Async. 


    =>----------------------------------------


    VANTAGENS

        Carregamento no Fundo:
            Não trava o script. É possivel utilizar o site enquanto o precessamento é realizado no fundo.


        Função ao Término
            Podemos ficar de olho no carregamento e executar uma função assim que ele terminar.


        Requisição ao Servidor
            Não precisamos recarregar a página por completo à cada requislição feita ao servidor.
*/

